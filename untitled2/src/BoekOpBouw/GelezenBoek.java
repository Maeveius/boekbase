package BoekOpBouw;

public class GelezenBoek {
    private boolean gelezen;

    public GelezenBoek(boolean gelezen) {
        this.gelezen = gelezen;
    }

    public boolean isGelezen() {
        return gelezen;
    }

    public void setGelezen(boolean gelezen) {
        this.gelezen = gelezen;
    }

}
